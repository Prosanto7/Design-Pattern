package FacadePattern;

public class CoolingController
{
    public void onCooling()
    {
        System.out.println("Cooling controller is started");
    }
    public void executeCooling()
    {
        System.out.println("COoling controller has started cooling");
    }
    public void endCooling()
    {
        System.out.println("Cooling controller is off");
    }
    
}
